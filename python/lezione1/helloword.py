# con l'asterisco posso commentare riga per riga
# esiste un modo per inserire i comandi ctrl + ù

print("Hello, World!")
print("Ciao, Dario")


# \n è il carattere per andare a capo
print("Questa di oggi è la seconda lezione di \nPython")

# Posso commentare più stringhe separate da una virgola
print("Primo argomento", "Secondo argomento", "Terzo argomento")

# Posso anche passare degli argomenti accessori oltre alle stringhe classiche 
# uso il sep, cioè il separator
print("Dario", "Paolo", "Laura", "Tina", sep="-")
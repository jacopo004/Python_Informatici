# permetti all'utente di inserire due numeri interi e calcola il risultato delle seguenti operazioni
# + - * / **2

num1 = int(input("Inserisci un numero intero "))
num2 = int(input("Inserisci un altro numero intero "))

somma = num1 + num2
print("La somma è:", somma)

sottr = num1 - num2
print("La sottrazione è:", sottr)

molt = num1 * num2
print("La moltiplicazione è:", molt)

div = num1 / num2
print("La divisione è:", div)

pot1 = num1 **2
print("Potenza1:", pot1)

pot2 = num2 **2
print("Potenza2:", pot2)